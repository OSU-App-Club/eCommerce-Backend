const dbwrapper = require("./db-wrapper.js");

const knexConfig = {
  client: "mysql",
  connection: {
    host : '127.0.0.1',
    port : 3306,
    user : 'your_database_user',
    password : 'your_database_password',
    database : 'myapp_test'
  }
}

const db = new dbwrapper(knexConfig);

db.getLastTransaction();











const express = require('express')
const app = express()
const port = 3000

